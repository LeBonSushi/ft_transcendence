export function ThemeToggleItem() {
  const { theme, setTheme } = useTheme()
  const [mounted, setMounted] = useState(false)
  const [isOpen, setIsOpen] = useState(false)

  useEffect(() => {
    setMounted(true)
  }, [])

  if (!mounted) {
    return null
  }

  const currentTheme = themes.find(t => t.value === theme) || themes[2]

  return (
    <div className="w-full flex items-center justify-between gap-4">
      <h3 className="text-sm font-medium whitespace-nowrap">Appearance</h3>
      <div className="relative inline-block flex-1 max-w-xs">
        <button
          onClick={() => setIsOpen(!isOpen)}
          className="w-full px-4 py-2 pr-10 rounded-md bg-background text-foreground border border-border focus:outline-none focus:ring-2 focus:ring-primary cursor-pointer flex items-center gap-2 text-left hover:bg-muted transition-colors"
        >
          <span>{currentTheme.icon}</span>
          <span>{currentTheme.label}</span>
        </button>
        <div className="pointer-events-none absolute inset-y-0 right-0 flex items-center px-3 text-muted-foreground">
          <svg 
            className={`h-4 w-4 transition-transform ${isOpen ? 'rotate-180' : ''}`} 
            fill="none" 
            stroke="currentColor" 
            viewBox="0 0 24 24"
          >
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 9l-7 7-7-7" />
          </svg>
        </div>
        
        {isOpen && (
          <>
            <div 
              className="fixed inset-0 z-10" 
              onClick={() => setIsOpen(false)}
            />
            <div className="absolute z-20 mt-1 w-full rounded-md bg-popover border border-border shadow-lg overflow-hidden">
              {themes.map((t) => (
                <button
                  key={t.value}
                  onClick={() => {
                    setTheme(t.value)
                    setIsOpen(false)
                  }}
                  className={`w-full px-4 py-2 text-left flex items-center gap-2 transition-colors ${
                    theme === t.value 
                      ? 'bg-muted text-primary' 
                      : 'text-foreground hover:bg-muted'
                  }`}
                >
                  <span className="text-lg">{t.icon}</span>
                  <span>{t.label}</span>
                  {theme === t.value && (
                    <svg className="ml-auto h-4 w-4" fill="currentColor" viewBox="0 0 20 20">
                      <path fillRule="evenodd" d="M16.707 5.293a1 1 0 010 1.414l-8 8a1 1 0 01-1.414 0l-4-4a1 1 0 011.414-1.414L8 12.586l7.293-7.293a1 1 0 011.414 0z" clipRule="evenodd" />
                    </svg>
                  )}
                </button>
              ))}
            </div>
          </>
        )}
      </div>
    </div>
  )
}
